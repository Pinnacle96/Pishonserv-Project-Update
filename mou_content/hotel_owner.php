<h3>Parties’ Obligations</h3>

<h4>Hotel Owner Obligations</h4>
<ul>
    <li><strong>Compliance with Rules:</strong> The Hotel Owner must adhere to Pishonserv’s guidelines for hotel property listings, ensuring accurate room descriptions, clear photos, truthful pricing, and real-time availability.</li>
    <li><strong>Fraud Prevention:</strong> The Hotel Owner must verify ownership or authorization before listing. No fraudulent or unauthorized listings are permitted.</li>
    <li><strong>Guest Satisfaction:</strong> The Hotel Owner shall maintain service and room standards as advertised, providing guests with quality accommodation and hospitality.</li>
    <li><strong>Updates:</strong> Room availability, rates, and amenities must be updated promptly to reflect changes or unavailability.</li>
    <li><strong>Legal Compliance:</strong> The Hotel Owner must comply with all local laws and hospitality regulations, including tax and licensing requirements.</li>
    <li><strong>Accuracy & Integrity:</strong> No false or misleading information may be provided regarding the hotel, services, or terms.</li>
    <li><strong>Brand Integrity:</strong> The Hotel Owner shall not engage in conduct that damages Pishonserv’s reputation or misrepresents affiliation beyond platform participation.</li>
</ul>

<h3>Remuneration</h3>
<ul>
    <li>Pishonserv will deduct its agreed service fee from customer payments before remitting the balance to the Hotel Owner.</li>
    <li>Remittance to the Hotel Owner will occur within the specified timeframe after guest check-in or confirmed booking completion.</li>
    <li>The Hotel Owner agrees to Pishonserv’s refund policy in cases of proven fraud, misrepresentation, or unsatisfactory service.</li>
    <li>Refunds attributable to the Hotel Owner’s fault may be deducted from payouts.</li>
</ul>

<h3>Warranties</h3>
<p>Each party warrants that it will perform its obligations with due diligence, professionalism, and efficiency consistent with accepted industry standards.</p>

<h3>Termination</h3>
<ul>
    <li>Pishonserv may suspend or terminate the Hotel Owner’s account for non-compliance with this MoU or platform policies.</li>
    <li>The Hotel Owner may terminate this MoU by written notice, subject to completion of any outstanding obligations.</li>
</ul>

<h3>Indemnity</h3>
<ul>
    <li>Each party shall indemnify and hold the other harmless for losses arising from its own negligence, misconduct, fraud, dishonesty, lack of good faith, or breach of this MoU.</li>
    <li>The Hotel Owner shall indemnify Pishonserv against claims arising from fraud, misrepresentation, or breach of this MoU.</li>
    <li>Pishonserv may suspend or terminate accounts if fraudulent activity is suspected.</li>
    <li>The Hotel Owner grants Pishonserv a non-exclusive license to use hotel images, descriptions, and promotional materials for marketing on the Platform.</li>
</ul>

<h3>Legal Effect</h3>
<p>This MoU governs the relationship between the parties regarding Pishonserv’s role as an advertising and booking platform for hotels, including property listings, reservations, sales/marketing, and payment facilitation.</p>
