<h3>Parties’ Obligations</h3>

<h4>Developer Obligations</h4>
<ul>
    <li><strong>Compliance with Listing Rules:</strong> The Developer shall follow Pishonserv’s guidelines for project listings, including accurate descriptions, high-quality images/renders, truthful pricing, unit availability, and delivery timelines.</li>
    <li><strong>Authorization & Title:</strong> The Developer shall verify ownership/authorization and the legal status of each project before listing (e.g., approvals, permits, titles, consents).</li>
    <li><strong>Customer Experience:</strong> The Developer commits to maintaining standards as advertised and to professional handling of all buyer/lead enquiries.</li>
    <li><strong>Project Updates:</strong> Listings must be kept current; material changes (pricing, inventory, specifications, delivery dates) shall be updated promptly.</li>
    <li><strong>Regulatory Compliance:</strong> The Developer shall comply with all applicable laws and regulations governing project development and unit sales.</li>
    <li><strong>Prohibited Conduct:</strong> No fraudulent, non-existent, or unauthorized projects may be listed. No false or misleading information about the project, units, terms, or incentives.</li>
    <li><strong>Brand Integrity:</strong> The Developer shall not engage in activities that harm Pishonserv’s reputation or misrepresent affiliation beyond platform participation.</li>
</ul>

<h3>Remuneration</h3>
<ul>
    <li>Pishonserv will deduct its agreed service fee from customer payments before remitting the balance to the Developer.</li>
    <li>Remittance to the Developer will occur within the specified timeframe after transaction completion or milestone as applicable.</li>
    <li>The Developer agrees to Pishonserv’s refund policy for proven fraud, misrepresentation, or materially unsatisfactory service relative to the listing.</li>
    <li>Refunds attributable to the Developer’s fault may be deducted from the Developer’s payout.</li>
</ul>

<h3>Warranties</h3>
<p>Each party warrants it will perform its obligations with due diligence, professionalism, and efficiency in line with generally accepted professional standards.</p>

<h3>Termination</h3>
<ul>
    <li>Pishonserv may suspend or terminate the Developer’s account for non-compliance with this MoU or platform policies.</li>
    <li>The Developer may terminate this MoU by written notice, subject to the fulfillment of any outstanding obligations.</li>
</ul>

<h3>Indemnity</h3>
<ul>
    <li>Each party shall indemnify and hold the other harmless for losses arising from its own negligence, omission, willful misconduct, fraud, dishonesty, lack of good faith, or breach of this MoU.</li>
    <li>The Developer shall indemnify Pishonserv against claims arising from fraud, misrepresentation, or breach of this MoU.</li>
    <li>Pishonserv may suspend or terminate accounts if fraudulent activity is suspected.</li>
    <li>The Developer grants Pishonserv a non-exclusive license to use project images, renders, and descriptions for marketing on the Platform.</li>
</ul>

<h3>Legal Effect</h3>
<p>This MoU governs the parties’ relationship regarding Pishonserv’s role as an advertising platform for real estate projects and developments, including listings, sales/marketing, and payment facilitation.</p>
