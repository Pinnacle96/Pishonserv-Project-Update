<h3>Purpose</h3>
<p>
This MoU sets the terms under which the Host lists short-let or serviced-apartment accommodation on Pishonserv, and Pishonserv provides advertising, booking, and payment facilitation services.
</p>

<h3>Host Obligations</h3>
<ul>
    <li><strong>Accuracy:</strong> Provide truthful, current, and complete details for each listing (photos, descriptions, amenities, pricing, taxes/fees, house rules, check-in/out windows).</li>
    <li><strong>Authorization:</strong> List only properties the Host owns or is duly authorized to manage; ensure multi-unit/building policies allow short-let activity.</li>
    <li><strong>Up-to-Date Availability:</strong> Keep calendars, pricing, and minimum/maximum stay rules current; promptly block unavailable dates.</li>
    <li><strong>Responsiveness:</strong> Respond to booking requests and guest messages in a timely and professional manner.</li>
    <li><strong>No Misrepresentation:</strong> Do not publish misleading information, bait-pricing, or duplicate/ghost listings.</li>
</ul>

<h3>Guest Experience & Operations</h3>
<ul>
    <li><strong>Check-in/Out:</strong> Provide clear instructions, access details, and a reliable point of contact; honor confirmed reservations.</li>
    <li><strong>Cleanliness & Turnover:</strong> Ensure the space is clean, safe, and as advertised at check-in; perform professional turnover between stays.</li>
    <li><strong>Amenities & Supplies:</strong> Provide the amenities listed (e.g., Wi-Fi speed, utilities, linen, toiletries) and maintain them in good working order.</li>
    <li><strong>Issue Resolution:</strong> Use best efforts to remedy guest issues quickly; document resolutions via the Platform’s messaging where possible.</li>
</ul>

<h3>Safety, Compliance & Insurance</h3>
<ul>
    <li><strong>Legal Compliance:</strong> Comply with all laws and regulations applicable to short-term lodging (permits, zoning, HOA/estate rules, taxes, safety codes).</li>
    <li><strong>Safety Standards:</strong> Maintain functional locks, lighting, and (where applicable) smoke/CO detectors, fire extinguishers, and emergency info.</li>
    <li><strong>Insurance:</strong> Maintain adequate property and liability insurance appropriate for short-let operations.</li>
</ul>

<h3>Fees & Payments</h3>
<ul>
    <li><strong>Commission:</strong> Pishonserv deducts the agreed service fee (<?= '[Insert %]' ?>) per completed booking before remitting the balance to the Host.</li>
    <li><strong>Payout Timing:</strong> Remittance occurs within the specified window after guest check-in or booking completion, subject to chargebacks/refunds.</li>
    <li><strong>Taxes:</strong> The Host is responsible for any applicable taxes, levies, or remittances required by law.</li>
</ul>

<h3>Cancellations & Refunds</h3>
<ul>
    <li><strong>Policy Disclosure:</strong> Specify a clear cancellation policy (flexible/moderate/strict or custom); it must be visible on the listing.</li>
    <li><strong>Guest Remedies:</strong> For material misrepresentation or service failure, refunds/adjustments may be granted under the Platform’s refund policy.</li>
    <li><strong>Host-Initiated Cancellations:</strong> Repeated or last-minute Host cancellations may lead to penalties, lowered ranking, or account action.</li>
</ul>

<h3>House Rules, Deposits & Damage</h3>
<ul>
    <li><strong>Rules:</strong> Publish reasonable house rules (occupancy limits, smoking/pets, noise/quiet hours); enforce lawfully and consistently.</li>
    <li><strong>Security Deposit:</strong> If used, disclose the amount (<?= '[Insert ₦/USD amount]' ?>) and conditions up front; apply fairly and document any claims with evidence.</li>
    <li><strong>Damage/Extra Cleaning:</strong> Claims must be evidenced (photos/receipts) and submitted through the Platform’s process.</li>
</ul>

<h3>Data & Communications</h3>
<ul>
    <li><strong>In-Platform Messaging:</strong> Conduct booking-related communications through the Platform where feasible for auditability.</li>
    <li><strong>Privacy:</strong> Handle guest data in compliance with applicable privacy laws and Platform policies; no unauthorized marketing or data sharing.</li>
</ul>

<h3>Termination & Account Actions</h3>
<ul>
    <li><strong>Platform Enforcement:</strong> Pishonserv may suspend or terminate accounts for fraud, safety risks, repeated cancellations, poor conduct, or policy breaches.</li>
    <li><strong>Host Termination:</strong> The Host may terminate with written notice, subject to fulfilling accepted bookings and outstanding obligations.</li>
</ul>

<h3>Indemnity</h3>
<ul>
    <li>The Host shall indemnify and hold Pishonserv harmless for claims and losses arising from the Host’s negligence, misrepresentation, legal non-compliance, or property conditions.</li>
    <li>Pishonserv may suspend/withhold payouts where fraud or policy violations are reasonably suspected pending investigation.</li>
</ul>

<h3>Legal Effect</h3>
<p>
This MoU governs the parties’ relationship regarding the listing and operation of short-term accommodation through Pishonserv, including advertising, bookings, guest communications, and payment facilitation.
</p>
