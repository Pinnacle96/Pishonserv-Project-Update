<h3>Parties’ Obligations</h3>

<h4>Agent Obligations</h4>
<ul>
    <li><strong>Compliance with Rules:</strong> The Agent must follow Pishonserv’s guidelines for agent-listed properties and leads, including accurate descriptions, high-quality photos, correct pricing, and current availability status.</li>
    <li><strong>Fraud Prevention:</strong> The Agent must verify ownership or authorization before listing any property or lead. No fraudulent, non-existent, or unauthorized listings are permitted.</li>
    <li><strong>Customer Satisfaction:</strong> The Agent shall maintain property standards as advertised and provide professional client handling.</li>
    <li><strong>Updates:</strong> Listings must be updated promptly to reflect changes or unavailability.</li>
    <li><strong>Legal Compliance:</strong> The Agent shall comply with applicable laws and regulations governing agency services and negotiations.</li>
    <li><strong>Accuracy & Integrity:</strong> The Agent shall not provide false or misleading information, misrepresent terms, or engage in conduct that harms Pishonserv’s reputation or customer trust.</li>
</ul>

<h3>Remuneration</h3>
<ul>
    <li>Pishonserv will deduct its agreed service fee from the customer payment before remitting the balance to the Agent.</li>
    <li>Remittance to the Agent will occur within the specified timeframe after customer check-in or transaction completion.</li>
    <li>The Agent agrees to Pishonserv’s refund policy, which applies in cases of proven fraud, misrepresentation, or unsatisfactory service.</li>
    <li>Refunds issued due to the Agent’s fault may be deducted from the Agent’s payout.</li>
</ul>

<h3>Warranties</h3>
<p>Each party warrants that it will perform its obligations with due diligence, professionalism, and efficiency, consistent with generally accepted professional standards.</p>

<h3>Termination</h3>
<ul>
    <li>Pishonserv may suspend or terminate the Agent’s account for non-compliance with this MoU or platform policies.</li>
    <li>The Agent may terminate this MoU by written notice, subject to fulfillment of any outstanding obligations.</li>
</ul>

<h3>Indemnity</h3>
<ul>
    <li>Each party shall indemnify and hold the other harmless for losses arising from its own negligence, omission, willful misconduct, fraud, dishonesty, lack of good faith, or breach of this MoU.</li>
    <li>The Agent shall indemnify Pishonserv against claims arising from fraud, misrepresentation, or breach of this MoU.</li>
    <li>Pishonserv may suspend or terminate accounts if fraudulent activity is suspected.</li>
    <li>The Agent grants Pishonserv a non-exclusive license to use property images and descriptions for marketing on the Platform.</li>
</ul>

<h3>Legal Effect</h3>
<p>This MoU governs the business relationship between the parties regarding Pishonserv’s role as an advertising platform for property listings and leads, sales, marketing, and payment facilitation.</p>
